        <h2>¿Qué son las cookies?</h2>

        <p>Las cookies son archivos que se pueden descargar en su equipo a través de las páginas web. Son herramientas
            que tienen un papel esencial para la prestación de numerosos servicios de la sociedad de la información.
            Entre otros, permiten a una página web almacenar y recuperar información sobre los hábitos de navegación de
            un usuario o de su equipo y, dependiendo de la información obtenida, se pueden utilizar para reconocer al
            usuario y mejorar el servicio ofrecido. Las cookies se asocian únicamente a un usuario anónimo y su
            ordenador o dispositivo, y no proporcionan referencias que permitan conocer sus datos personales.</p>

        <h2>¿Por qué son importantes?</h2>

        <p>Desde un punto de vista técnico, permiten que el Sitio web funcione de forma más ágil y adaptada a las
            preferencias de los usuarios, como por ejemplo almacenar el idioma, la moneda del país o detectar el
            dispositivo de acceso.</p>

        <p>Establecen niveles de protección y seguridad que impiden o dificultan ciberataques contra el Sitio web o sus
            usuarios.</p>

        <p>Permiten que los gestores de los medios puedan conocer datos estadísticos recopilados en las Cookies para
            mejorar la calidad y experiencia de sus servicios.</p>

        <p>Sirven para optimizar la publicidad que mostramos a los usuarios, ofreciendo la que más se ajusta a sus
            intereses.</p>

        <h2>Tipos de cookies</h2>

        <p><strong>1. Cookies propias:</strong> Aquéllas que se envían al equipo terminal del usuario desde un equipo o
            dominio gestionado por el propio editor y desde el que se presta el servicio solicitado por el usuario.</p>

        <p><strong>2. Cookies de terceros:</strong> Aquéllas que se envían al equipo terminal del usuario desde un
            equipo o dominio que no es gestionado por el editor, sino por otra entidad que trata los datos obtenidos
            través de las cookies.</p>

        <p><strong>3. Cookies de sesión:</strong> Diseñadas para recabar y almacenar datos mientras el usuario accede a
            una página web. Se suelen emplear para almacenar información que solo interesa conservar para la prestación
            del servicio solicitado por el usuario en una sola ocasión (p.e. una lista de productos adquiridos).</p>

        <p><strong>4. Cookies persistentes:</strong> Los datos siguen almacenados en el terminal y pueden ser accedidos
            y tratados durante un periodo definido por el responsable de la cookie, y que puede ir de unos minutos a
            varios años.</p>

        <p><strong>5. Cookies técnicas:</strong> Aquellas que permiten al usuario la navegación a través de una página
            web, plataforma o aplicación y la utilización de las diferentes opciones o servicios que en ella existan
            como, por ejemplo, controlar el tráfico y la comunicación de datos, identificar la sesión, acceder a partes
            de acceso restringido, recordar los elementos que integran un pedido, realizar el proceso de compra de un
            pedido, realizar la solicitud de inscripción o participación en un evento, utilizar elementos de seguridad
            durante la navegación, almacenar contenidos para la difusión de vídeos o sonido o compartir contenidos a
            través de redes sociales.</p>

        <p><strong>6. Cookies de personalización:</strong> Permiten al usuario acceder al servicio con algunas
            características de carácter general predefinidas en función de una serie de criterios en el terminal del
            usuario como por ejemplo serían el idioma, el tipo de navegador a través del cual accede al servicio, la
            configuración regional desde donde accede al servicio, etc.</p>

        <p><strong>7. Cookies de análisis:</strong> Aquellas que bien tratadas por nosotros o por terceros, permiten al
            responsable de las mismas el seguimiento y análisis del comportamiento de los usuarios de los sitios web a
            los que están vinculadas. La información recogida mediante este tipo de cookies se utiliza en la medición de
            la actividad de los sitios web, aplicación o plataforma y para la elaboración de perfiles de navegación de
            los usuarios de dichos sitios, aplicaciones y plataformas, con el fin de introducir mejoras en función del
            análisis de los datos de uso que hacen los usuarios del servicio.</p>

        <p><strong>8. Cookies publicitarias:</strong> Permiten la gestión, de la forma más eficaz posible, de los
            espacios publicitarios que, en su caso, el editor haya incluido en una página web, aplicación o plataforma
            desde la que presta el servicio solicitado en base a criterios como el contenido editado o la frecuencia en
            la que se muestran los anuncios.</p>

        <p><strong>9. Cookies de publicidad comportamental:</strong> Son aquéllas que permiten la gestión, de la forma
            más eficaz posible, de los espacios publicitarios que, en su caso, el editor haya incluido en una página
            web, aplicación o plataforma desde la que presta el servicio solicitado. Estas cookies almacenan información
            del comportamiento de los usuarios obtenida a través de la observación continuada de sus hábitos de
            navegación, lo que permite desarrollar un perfil específico para mostrar publicidad en función del mismo.
            Asimismo, es posible que al visitar alguna página web o al abrir algún email donde se publique algún anuncio
            o alguna promoción sobre nuestros productos o servicios se instale en tu navegador alguna cookie que nos
            sirve para mostrarte posteriormente publicidad relacionada con la búsqueda que hayas realizado, desarrollar
            un control de nuestros anuncios en relación, por ejemplo, con el número de veces que son vistos, dónde
            aparecen, a qué hora se ven, etc.</p>

        <p><strong>10. Cookies de redes sociales externas:</strong> Se utilizan para que los visitantes puedan
            interactuar con el contenido de diferentes plataformas sociales (Facebook, YouTube, Twitter, LinkedIn, etc.)
            y que se generen únicamente para los usuarios de dichas redes sociales. Las condiciones de utilización de
            estas cookies y la información recopilada se regula por la política de privacidad de la plataforma social
            correspondiente.</p>

